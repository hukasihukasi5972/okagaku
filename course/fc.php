        <div class="tab-pane row" id="work-fc">
          <h3>将来の仕事</h3>
          <dl>
            <dt>■ ファッションデザイナー</dt>
            <dd>次々と変化する流行や消費動向を読み取り、能力・素材知識・センスを活かし新しいデザイン、新しいファッションを生み出していく仕事。</dd>
          </dl>
          <dl>
            <dt>■ パタンナー</dt>
            <dd>デザイナーが描いたデザイン画を型紙に起こす。平面のデザインを立体的な洋服にする、洋服の設計士。</dd>
          </dl>
          <dl>
            <dt>■ リフォーマー</dt>
            <dd>洋服のデザインやサイズの直しなどを行う。洋裁・和裁の技術とかけはぎ・修理・寸法直しの技術が必要。</dd>
          </dl>
          <dl>
            <dt>■ ファッションアドバイザー</dt>
            <dd>店舗で接客および商品の販売を行う。コーディネートや着こなしを提案できる商品知識とファッションセンスが必要。</dd>
          </dl>
          <dl>
            <dt>■ ソーイングスタッフ</dt>
            <dd>製品の縫製を専門に行う。仕上がりの美しさを要求される技術職。素材に対する知識、縫製知識が必要。</dd>
          </dl>
          <dl>
            <dt>■ テキスタイルデザイナー</dt>
            <dd>糸の企画から織地、染色や各種加工、プリントデザインなどを一貫して行います。</dd>
          </dl>
          <h3>主な取得可能資格</h3>
          <dl>
            <dt>■ パターンメーキング技術検定試験</dt>
            <dd>（財）日本ファッション振興協会が実施している検定。パターンの基礎知識・工業パターン・グレーディング・素材の違いによるパターン操作等を、筆記と実技試験で評価されます。精度の高いパターンメイキングの技術を習得した者は、アパレルデザイン制作現場で高い評価を得ることができるため将来パタンナーになりたい人は取得するべき資格です。</dd>
          </dl>
          <dl>
            <dt>■ 洋裁技術検定</dt>
            <dd>（財）日本ファッション振興協会が実施している検定。衣類制作の基本となる洋裁の知識と技術を判断する検定であり、就学年数により受験級が初級・中級・上級とランクアップします。在学中に受験すると実技試験が免除されます。</dd>
          </dl>
          <dl>
            <dt>■ ＡＦＴ色彩検定</dt>
            <dd>文部科学省が認定している検定。従来、感性だけによるものと捉えられてきた「色」を理論的に、統計的に学ぶ事により色彩の実践的活用能力を身につけることができます。在学中に3～1級の取得が可能であり、ファッション業界の他、フード、インテリア、デザイン業界でも幅広く活用出来る資格です。</dd>
          </dl>
          <dl>
            <dt>■ ドレメ式教員認定</dt>
            <dd>ファッションクリエーターコース2年卒業以上で、校長の推薦を受けた者が取得できる資格です。ファッション系の学校の教員として勤務し、勤務年数により級が上がります。</dd>
          </dl>
          <dl>
            <dt>■ 技能検定</dt>
            <dd>厚生労働省が実施している検定。洋裁技術に対する社会一般の評価を高め、働く人の技能と地位の向上図ることが目的。確かな技術の証として各職場で評価されている国家資格です。</dd>
          </dl>
