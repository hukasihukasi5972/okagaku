          <h3>地域ブランディング</h3>
          <p>ファッション科では、地域の資材や素材を活用し、長野ならではのショーを開催しています。また、地元のデザイナーやアーティストとコラボレートし、ファッションにとどまらずグラフィックや映像、空間演出とジャンルの異なる表現方法で高いクオリティを実現しています。今後も校内の枠にとらわれず、地域と協力して地元を盛り上げるイベントを数多く行っていきます。</p>
