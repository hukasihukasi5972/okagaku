          <h3>産官学コラボレーション</h3>
          <p>当校では、実践教育として、様々な企業・団体と協力して社会に通じるデザインを発信しつづけています。クライアントの求めるデザインを企画立案、提案、制作することで、市場が求めるニーズを知り、プロダクトアウト（制作者本意）にならないマーケットイン（ユーザー本意）の思考を学ぶことを目的としています。</p>
          <p><img src="img/fc/collabo_fc_01.jpg" alt="産官学コラボ実例"></p>
          <p><img src="img/fc/collabo_fc_02.jpg" alt="産官学コラボ実例"></p>
