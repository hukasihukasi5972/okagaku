          <h3>将来の仕事</h3>
          <dl>
            <dt>■ スタイリスト</dt>
            <dd>雑誌や広告の写真撮影、テレビ番組やCM制作等の際に、出演者の衣装の手配や選択を行い、ファッション全般をコーディネートする仕事。</dd>
          </dl>
          <dl>
            <dt>■ ファッションアドバイザー</dt>
            <dd>店舗で接客および商品の販売を行う。コーディネートや着こなしを提案できる商品知識とファッションセンスが必要。</dd>
          </dl>
          <dl>
            <dt>■ プレス</dt>
            <dd>雑誌やT Vなどのマスコミに製品やブランドをPRし、商品の貸出しなどを行う。マーケットやメディアを分析する能力が要求される。</dd>
          </dl>
          <dl>
            <dt>■ マーチャンダイザー</dt>
            <dd>企画からデザイン、生産、販売まで全体を把握し、商品を市場に出す。流行や消費動向を読み取る能力、スタッフを統率する力も必要。</dd>
          </dl>
          <dl>
            <dt>■ ショップマネージャー</dt>
            <dd>店長・各店舗の経営責任者。棚卸し管理、搬入・搬出の管理、売り場作り、売場売上集計、メーカーとの交渉など、多岐にわたる業務をこなす。</dd>
          </dl>
          <dl>
            <dt>■ バイヤー</dt>
            <dd>ショップの営業方針や消費者のニーズに対した商品を、国内各所や、場合によっては海外からも選び出して買い付けてくる仕入れのプロ。</dd>
          </dl>
          <dl>
            <dt>■ ディスプレイデザイナー</dt>
            <dd>デパートやショップなどのショーウィンドウで、小道具や照明などを使い商品を引き立てるために空間演出をする仕事。</dd>
          </dl>
          <dl>
            <dt>■ ドレスコーディネーター</dt>
            <dd>ウェディングドレスからブーケなどの小物まで、ブライダル衣装をトータルでコーディネートする仕事。</dd>
          </dl>
