          <h3>将来の仕事</h3>
          <dl>
            <dt>■ グラフィックデザイナー</dt>
            <dd>平面デザインをつくる仕事。範囲はかなり広く、ポスター・チラシ、新聞広告、雑誌、書籍、ＣＤジャケット、商品パッケージなどがある。最近は作業のほとんどをPCを使って行う</dd>
          </dl>
          <dl>
            <dt>■ イラストレーター</dt>
            <dd>雑誌や書籍、ポスターや広告、カタログ、パンフレットなどの印刷物や、HPなどに掲載するイラストを描く仕事。PCのスキルも必要。</dd>
          </dl>
          <dl>
            <dt>■ エディトリアルデザイナー</dt>
            <dd>書籍、雑誌、新聞、マンガ、カタログ、パンフレット、各種マニュアルなどの文章が含まれている紙面を、デザイン的に美しくかつ読みやすくレイアウトする仕事。</dd>
          </dl>
          <dl>
            <dt>■ キャラクターデザイナー</dt>
            <dd>アニメやゲーム、小物グッズなどの制作現場にて、キャラクターデザインを行う。</dd>
          </dl>
          <dl>
            <dt>■ DTPオペレーター</dt>
            <dd>雑誌・書籍や新聞、その他印刷物をつくるために、印刷の際のもととなる版下・製版データをPCで制作する仕事。</dd>
          </dl>
          <dl>
            <dt>■ 雑誌・出版編集者</dt>
            <dd>雑誌の特集企画や、誌面わり、ライター、グラフィックデザイナー、カメラマンの手配等、雑誌等を創る際のプロデューサー的存在。</dd>
          </dl>
          <dl>
            <dt>■ 絵本作家</dt>
            <dd>絵本を表現手段として活躍する作家のこと。 絵本作家が絵本の文、または文と絵を制作するのに対して、絵本の絵のみを担当する場合は絵本画家と呼びばれる。</dd>
          </dl>
          <dl>
            <dt>■ Webディレクター</dt>
            <dd>依頼主の要望を汲んで制作プランを作成し、デザイナーなどの作品をまとめてサイト構築を行い、ホームページを制作する仕事。</dd>
          </dl>
          <h3>主な取得可能資格</h3>
          <dl>
            <dt>■ llustratorクリエイター能力認定試験</dt>
            <dd>サーティファイソフトウェア活用能力認定委員会が実施している資格試験。 ドキュメントデザインに関する知識を持ち、Illustratorを使って指示されたテーマや素材から、仕様に従ったコンテンツを作成する能力を認定します。認定基準はスタンダードからエキスパートまで。Illustratorを利用したグラフィック作成関連の仕事、デザイナーやWebコンテンツ作成者、あるいはイラストレーターやDTPオペレーターなど、グラフィックを扱う職種全般に活かすことが可能です。</dd>
          </dl>
          <dl>
            <dt>■ Photoshopクリエイター能力認定試験</dt>
            <dd>サーティファイソフトウェア活用能力認定委員会が行う資格試験。デジタル画像編集技術に関する知識を持ち、Photoshopを使って提示されたテーマや素材から、仕様に従ったコンテンツを制作する能力を認定します。認定基準はスタンダードからエキスパートまで。Photoshopを利用したグラフィック作成関連の仕事、デザイナーやマルチメディア、Webコンテンツ作成者など、デジタルグラフィックを扱う職種全般に活かすことが可能です。</dd>
          </dl>
          <dl>
            <dt>■ Webクリエイター能力認定試験</dt>
            <dd>サーティファイソフトウェア活用能力認定委員会が行う資格試験。Webページを制作するためのHTMLファイルのコーディング能力、および基本的なデザイン能力を有し、Webサイトを構築するためのクリエイター能力を認定する資格です。認定基準はスタンダードからエキスパートまで。Webクリエイターを目指す上で必要最低限な認定試験です。</dd>
          </dl>
          <dl>
            <dt>■ ＡＦＴ色彩検定</dt>
            <dd>文部科学省が認定している検定。従来、感性だけによるものと捉えられてきた「色」を理論的に、統計的に学ぶ事により色彩の実践的活用能力を身につけることができます。在学中に3～1級の取得が可能であり、ファッション業界の他、フード、インテリア、デザイン業界でも幅広く活用出来る資格です。</dd>
          </dl>
          <dl>
            <dt>■ 技能検定</dt>
            <dd>厚生労働省が実施している検定。洋裁技術に対する社会一般の評価を高め、働く人の技能と地位の向上図ることが目的。確かな技術の証として各職場で評価されている国家資格です。</dd>
          </dl>
