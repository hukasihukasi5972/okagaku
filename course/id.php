          <h3>将来の仕事</h3>
          <dl>
            <dt>■ ゲームキャラクターデザイナー</dt>
            <dd>ゲーム企画をもとにゲームのキャラクター、モンスターなどをデザインします。新人でも小物モンスターや背景の細かいパーツなどをデザインする余地があり、徐々に重要な部分を任されるようになります。</dd>
          </dl>
          <dl>
            <dt>■ ライトノベルイラストレーター</dt>
            <dd>ライトノベルの内容をもとに表紙、挿絵などをデザインします。読者層を考え、本を手に取ってもらえるようなデザインにします。有名なイラストレーターが描く以外にも、コンテストで新人が選ばれる場合もあります。</dd>
          </dl>
          <dl>
            <dt>■ パッケージイラストレーター</dt>
            <dd>各種の土産物や食品、雑貨その他のパッケージに描かれているイラストを制作します。ご当地ものや季節限定ものなど、同じ商品でも複数のイラストが制作される場合もあります。メーカーに所属する場合もあります。</dd>
          </dl>
          <dl>
            <dt>■ マスコットキャラデザイナー</dt>
            <dd>各地のゆるキャラ、萌えキャラのほかに企業、商品、イベントなどで固有のキャラクターがいる場合があります。パッケージイラストレーターの中のキャラクター部門ともいえます。</dd>
          </dl>
          <dl>
            <dt>■ CGグラフィッカー</dt>
            <dd>CGイラスト制作の仕事は、デザイン、下書き、清書、塗りと業務が分かれている場合もあります。デザインは苦手でも塗りは得意という方もいます。CGグラフィッカーは主に塗りだけを担当します。メーカーに所属する場合もあります。</dd>
          </dl>
          <dl>
            <dt>■ 絵師</dt>
            <dd>自分でイラストを制作して発表し、仕事を受注します。グループで活動する場合や会社に所属する場合もあります。</dd>
          </dl>
          <dl>
            <dt>■ Flashアニメーター</dt>
            <dd>Flashという動画作成ソフトを使ってアニメーションを制作します。写真やビデオ映像も編集に使えるので、Webや映像広告との相性が良く、動画のイラストレーターといえます。</dd>
          </dl>
          <dl>
            <dt>■ 映像編集者</dt>
            <dd>映像制作会社に所属し、指示をもとにTVCMや結婚式などのイベント映像などを制作します。ビデオ映像を編集するだけでなく、各種の編集ソフトを使って効果的な演出、テキスト、音源を加えます。</dd>
          </dl>
          <dl>
            <dt>■ 映像クリエーター</dt>
            <dd>映像制作会社から独立したり、映像コンテストで有名になったりして各種映像を自分で受注する場合もあります。グループで会社を興す場合もあります。動画の絵師といえます。</dd>
          </dl>
          <dl>
            <dt>■ 3Dデザイナー</dt>
            <dd>3Dソフトを使ってキャラクター、モンスター、背景などを作ります。ゲーム・映画・アニメ・映像制作業界のほかに、建設業界にも需要があります。</dd>
          </dl>
